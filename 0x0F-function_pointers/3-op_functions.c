#include "3-calc.h"

/**
  * op_add - calculate the sum of two integers
  * @a: first number
  * @b: second number
  * Return: sum of a and b
  */

int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - calculate the difference of two integers
* @a: first number
* @b: second number
* Return: the difference of a and b
*/

int op_sub(int a, int b)
{
	return (a - b);
}


/**
* op_mul - calculate the product of two integers
* @a: first number
* @b: second number
* Return: product of a and b
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_div - division of two integers a and b
* @a: first number
* @b: second number
* Return: result of  division of a and b
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - calculate the remainder of the division of two integers
* @a: first number
* @b: second number
* Return:the remander from division of a and b
*/

int op_mod(int a, int b)
{
	return (a % b);
}


